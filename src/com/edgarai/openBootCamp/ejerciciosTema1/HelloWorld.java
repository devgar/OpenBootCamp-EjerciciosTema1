package com.edgarai.openBootCamp.ejerciciosTema1;

public class HelloWorld {

    /**
     * Main function, prints examples of values using different java types
     * @param args the command line arguments
     * @author Edgar Albalate
     */
    public static void main(String[] args) {
        System.out.println("A continuaci√≥n se muestra ejemplos de los distintos tipos de Java");
        System.out.println("-----------------------------------------------------------------");

        // int type example
        int number = 10;
        System.out.println("Ejemplo de int:     " + number);

        // long type example
        long longNumber = 10L;
        System.out.println("Ejemplo de long:    " + longNumber);

        // float type example
        float floatNumber = 10.0f;
        System.out.println("Ejemplo de float:   " + floatNumber);

        // double type example
        double doubleNumber = 10.5;
        System.out.println("Ejemplo de double:  " + doubleNumber);

        // boolean type example
        boolean boolValue = true;
        System.out.println("Ejemplo de boolean: " + boolValue);

        // char type example
        char character = 'a';
        System.out.println("Ejemplo de char:    " + character);

        // String type example
        String text = "Hola, mundo!";
        System.out.println("Ejemplo de String:  " + text);

    }
}
